#ifndef ARM64_QEMU_VM_CONFIG_H
#define ARM64_QEMU_VM_CONFIG_H

#include "platform_qemu.h"

#define QEMU_LK_RAM_START		0x40000000UL
#define QEMU_LK_RAM_SIZE		0x02000000UL
#define QEMU_ZEPHYR_RAM_START		0x42000000UL
#define QEMU_ZEPHYR_RAM_SIZE		0x06000000UL
#define QEMU_LINUX_RAM_START		0x48000000UL
#define QEMU_LINUX_RAM_SIZE		0x08000000UL
#define QEMU_LINUX_KERNEL_LOAD_ADDR	(QEMU_LINUX_RAM_START + 0x00080000UL)
#define QEMU_LINUX_INITRD_LOAD_ADDR	(QEMU_LINUX_RAM_START + 0x04000000UL)
#define QEMU_LINUX_IMAGE_STAGE_ADDR	0x70000000UL
#define QEMU_LINUX_INITRD_STAGE_ADDR	0x74000000UL

#define QEMU_GUEST_GICD_BASE		0x08000000UL
#define QEMU_GUEST_GICD_SIZE		0x00010000UL
#define QEMU_GUEST_GITS_BASE		0x08080000UL
#define QEMU_GUEST_GITS_SIZE		0x00020000UL
#define QEMU_GUEST_GICR_BASE		0x080A0000UL
#define QEMU_GUEST_GICR_STRIDE		0x00020000UL
#define QEMU_GUEST_GICR_SIZE		(MAX_PCPU_NUM * QEMU_GUEST_GICR_STRIDE)

#define QEMU_GUEST_UART_BASE		0x09000000UL
#define QEMU_GUEST_UART_SIZE		0x00001000UL
#define QEMU_GUEST_UART_IRQ		33U

#endif /* ARM64_QEMU_VM_CONFIG_H */
